package com.algo;

public class CommonDisplay {
	public static void display(int[] arr) {
		for(int i=0;i<arr.length;i++) {
			System.out.print(arr[i]+"");
		}
		System.out.println();
		
	}
//	string display message
	public static void display1(String[] arr) {
		for(int i=0;i<arr.length;i++) {
			System.out.print(arr[i]+" ");
		}
		System.out.println();
		
	}
	

}
